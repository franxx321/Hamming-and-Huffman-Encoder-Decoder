    private void AbrirAButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirAButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser j = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int r = j.showSaveDialog(this);

        if (r == JFileChooser.APPROVE_OPTION) {
            path = j.getSelectedFile().getAbsolutePath();
            name = j.getSelectedFile().getName();
            name = name.substring(0, name.lastIndexOf('.'));
            ArchivoLabel.setText(path);
        } else {
            ArchivoLabel.setText("No se cargo nada");
        }

    }//GEN-LAST:event_AbrirAButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Huffman huf = new Huffman();
        huf.descomprimir(path,pathtable, name);
            try {
                File myFile = new File(name + ".dhu");
                String s = "", cadena = "";
                FileReader f = new FileReader(myFile);
                BufferedReader b = new BufferedReader(f);
                while ((cadena = b.readLine()) != null) {
                    s += cadena + "\n";
                }
                TextoArchivoTextArea.setText(s);
                TextoArchivoTextArea.setEnabled(true);
                 JOptionPane.showMessageDialog(this, "Â¡Archivo descompactado con exito!", "Exito", JOptionPane.INFORMATION_MESSAGE);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(DescompactarPanel.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(DescompactarPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

